
package basicjava;

public class UnaryOperatorDemo {
    public static void main(String[] args) {
        
        int x = 10;
        int result;
        
        result = +x;                // positvie number
        System.out.println("result = " +result);
        
        result = -x;                //negative number
        System.out.println("result = " +result);
        
        
        
        result = +x;
        System.out.println("result = " +result);
        
    }
}
